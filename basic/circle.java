package basic;

public class circle {
	public static void main(String[] args) {
		int r=4;
		float f = 3.14f;
		float r1=2*r;
		float r2=2*f*r;
		float r3 = 2*r*r;
		System.out.println("diameter of circle :"+r1);
		System.out.println("circumfrance of circle:"+r2);
		System.out.println("area of circle :"+r3);
}
}